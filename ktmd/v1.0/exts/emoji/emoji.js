	Emoji.prototype.processCharAttribute = function( aLineIn ) {

		var textIn  = aLineIn;
		var textOut = '';
		var matches = [];

		while ( matches = textIn.match( /^(.*?)\ \:(\S*?)\:\ (.*)$/ ) ) {	// :emoji:
			var emoji = this.emojis[ matches[ 2 ] ];
			if ( emoji ) {
				textOut += matches[ 1 ] + emoji;
				textIn   = matches[ 3 ];
			} else {
				textOut += matches[ 1 ] + ' EMOJI ';
				textIn   = matches[ 3 ];
			}
		}

		return( textOut + textIn );

	}


	ktmd.readyExtension( 'emoji', new Emoji() );
